package loops;

public class ForLoopTest {
public static void main(String[] args) {
// for loop

  //initialization	condition  increment/decrement
for(int i=0;        i<10;       i++) { // 0+1=1; 1+1=2; 2+1=3; 9+1=10; 
	             
	System.out.println(i);//execution
}
System.out.println("===================");
//9----0

for(int i=10; i>0; i--) {
	System.out.println(i);
}

}
}
